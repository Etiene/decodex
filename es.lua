Velocidad de pronunciación[editar]
En un estudio realizado por la Universidad de Lyon se compararon los idiomas: alemán, español, francés, inglés, italiano, japonés, mandarín y vietnamita. Se llegó a la conclusión de que solo el japonés es capaz de superar al español tanto en rapidez como en baja densidad de información por sílaba pronunciada. Las cifras para el idioma español fueron: 7,82 sílabas por segundo, frente a la media de 6,1 sílabas en inglés en esa misma fracción de tiempo, confirmando que la rapidez del idioma se debe a la adaptación a su estructura.346 [cita requerida]

Alfabeto[editar]
El alfabeto usado por el idioma español es el alfabeto latino, del que se emplean 27 letras: a, b, c, d, e, f, g, h, i, j, k, l, m, n, ñ, o, p, q, r, s, t, u, v, w, x, y, z.

En español moderno la «h» no se corresponde con ningún fonema (aunque en castellano antiguo y algunos dialectos regionales modernos sigue representando el fonema /h/). Todas las variantes de español cuentan con al menos 22 fonemas (17 consonánticos y cinco vocálicos), teniendo algunas variantes hasta 24 fonemas (dos fonemas que aparecen en español ibérico septentrional y no aparecen en todas las variantes son /θ/ y /ʎ/). Además, no hay correspondencia exacta entre el resto de letras y fonemas (por ejemplo, «c» = /k/ ante /a, o, u/ y /θ/ ante /e, i/ en España). Asimismo, se emplean también cinco dígrafos para representar otros tantos fonemas: «ch», «ll», «rr», «gu» y «qu», considerados estos dos últimos como variantes posicionales para los fonemas /g/ y /k/.347

Los dígrafos ch y ll fueron considerados como letras independientes del alfabeto español desde 1754 hasta 2010, por lo que ambos signos gráficos estuvieron listados aparte en los diccionarios de 1803 a 1994, debido a que mantienen una pronunciación propia que es distinta a la de las letras individuales que los componen (c y l). Asimismo la mayoría de hablantes de español hablan variedades que presentan yeísmo, bajo el cual la pronunciación de la ll es idéntica a la de la y cuando esta última se pronuncia como consonante, aunque su sonido sea tradicionalmente considerado un fonema lateral palatal.

La letra «r» puede representar el fonema /ɾ/ (final o inicio de sílaba media) o /r/ (inicial de palabra, o tras /-s/) tal como muestran los siguientes ejemplos:

dar ocas /'da'ɾokas/
da rocas /'da'rokas/
dar rocas /'daɾ'rokas/
Israel /isra'el/ (no /isɾa'el/)
En cambio el dígrafo «rr» siempre representa el fonema /r/.

