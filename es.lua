Valor económico del idioma español[editar]
Un estudio de una fundación privada sugiere que el valor económico del idioma español en España está cifrado en un 15,6 % del PIB del país. En la actualidad, tres millones y medio de personas poseen empleos directamente relacionados con el español, un millón más que la pasada década. Además, compartir la lengua española, explica que los intercambios comerciales de España con Iberoamérica, particularmente, se multiplican por 2,5 veces.358 Compartir el español aumenta un 290 % el comercio bilateral entre los países hispanohablantes.

También son factores de valor económico; la propia enseñanza de la lengua, la industria cultural, la edición de publicaciones, la gastronomía, la ciencia, la arquitectura, el deporte y el turismo.

Según el Instituto Cervantes, el número de turistas idiomáticos que llegan a España ha crecido, desde el 2000 hasta el 2007, un 137,6 %. Y el sector turístico español cifra en 462,5 millones de euros los ingresos del turismo idiomático en España en 2007. Los 237 600 estudiantes que llegaron a España en ese año destinaron 176,5 millones a los cursos de español, de los que el 86 % fue a parar a centros privados de idiomas y el porcentaje restante a las universidades.359

