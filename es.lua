La Real Academia Española (RAE) y el resto de academias asociadas son un organismo dedicado a la elaboración de reglas normativas para el idioma español y a trabajar por la unidad del idioma español en todos los territorios en los que se habla.361 Estas normas se ven plasmadas en varias obras de gramática, ortografía, y lexicografía, como son la Ortografía de la lengua española (1999), el Diccionario panhispánico de dudas (2005), la Nueva gramática de la lengua española (2009) o el Diccionario de la lengua española, con 23 ediciones publicadas entre 1780 y 2014. Estas academias forman la Asociación de Academias de la Lengua Española (ASALE), abarcando 23 academias362 en los siguientes países, por orden de fundación: España (1713), Colombia (1871), Ecuador (1874), México (1875), El Salvador (1876), Venezuela (1883), Chile (1885), Perú (1887), Guatemala (1887), Costa Rica (1923), Filipinas (1924), Panamá (1926), Cuba (1926), Paraguay (1927), República Dominicana (1927), Bolivia (1927), Nicaragua (1928), Argentina (1931), Uruguay (1943), Honduras (1949), Puerto Rico (1955), Estados Unidos (1973), y Guinea Ecuatorial (2013),363 admitida en la ASALE en 2016.364

No existen academias de la lengua española en otros países que tienen una intensa vinculación histórica con España (como Andorra y el Sahara Occidental) o están insertos en un entorno cultural hispanohablante (como Belice). Por otro lado, la Real Academia Española mantiene la tradición de nombrar académicos correspondientes a personalidades de países vinculados de una u otra manera con la lengua española.

