El español en países donde no es oficial[editar]
Estados Unidos[editar]

Distribución de los hispanohablantes en los Estados Unidos en el Censo de 2010.
Estados Unidos es el segundo país con más hablantes de español en el mundo después de México,111 112 con un avance progresivo del bilingüismo, sobre todo en los estados de California, Nuevo México y Texas, en los que existen programas oficiales bilingües de español para residentes procedentes de Hispanoamérica. Así por ejemplo, en California muchas actividades de gobierno, documentos y servicios están disponibles en español. La sección 1632 del Código Civil de California reconoce al idioma español como la lengua de la considerable y creciente comunidad hispana, de ahí que la ley Dymally-Alatorre instituya un bilingüismo inglés-español, sin la exclusión necesaria de otras lenguas. En el estado de Nuevo México, el español se utiliza incluso en la administración estatal, aunque ese estado no tiene ninguna lengua oficial establecida en su constitución. El español neomexicano hablado por hispanohablantes nativos del estado (no por inmigrantes recientes) se remonta a los tiempos de la colonización española en el siglo XVI y conserva numerosos arcaísmos. La Comisión de Derechos Civiles de Estados Unidos reconoce que en 1912 “los neomexicanos tuvieron éxito a la hora de proteger su herencia, insertando provisiones en su constitución que hacen del español una lengua oficial igual que el inglés”. [cita requerida] En Texas, el gobierno, a través de la sección 2054.116 del Código Gubernamental, ordena que las agencias estatales proporcionen la información en sus páginas web en español. [cita requerida] Otros estados de la Unión también reconocen la importancia del castellano en su territorio. El español tiene una larga historia en los Estados Unidos; muchos estados y accidentes geográficos tienen su nombre en ese idioma, pero el uso de la lengua española ha aumentado sobre todo por la inmigración proveniente del resto de América. El español también se concentra especialmente en ciudades cosmopolitas como Nueva York, Los Ángeles, Chicago, Miami, Houston, Dallas, San Antonio, Denver, Baltimore, Portland y Seattle. El español, además, es la lengua más enseñada en el país.113

Brasil[editar]
El español siempre ha sido importante en Brasil a causa de la proximidad y el comercio creciente con sus vecinos hispanoamericanos, siendo miembro de Mercosur, así como por la inmigración histórica de españoles e hispanoamericanos. En 2005, el Congreso Nacional de Brasil aprobó el decreto, firmado por el presidente, conocido como ley del español, que ofrece esta lengua como primera lengua extranjera de enseñanza en los colegios y liceos del país.114 El español es una lengua fácil de aprender para los brasileños, gracias a que el portugués es una lengua muy similar al español.115 En la zona fronteriza entre Brasil y Uruguay se habla una lengua mixta llamada portuñol.116 La constitución del estado de Río de Janeiro y una deliberación del gobierno de São Paulo incluyen el español de modo oficial en los centros de enseñanza secundaria. Así el artículo 317.3 de la constitución del estado de Río de Janeiro de 1989 declara: «La lengua española pasa a formar parte del núcleo obligatorio de disciplinas de todas las series del segundo grado de la red estatal de enseñanza, teniendo en cuenta primordialmente, lo que establece la constitución de la República en su artículo cuarto, párrafo único». Y el artículo 2 de la deliberación n.º 77 del consejo estatal de educación del gobierno del estado de Sao Paulo de 2008 reconoce: «El español es un componente curricular obligatorio, según la legislación federal en vigor, a ser desarrollado de acuerdo con los términos de las orientaciones contenidas en la indicación cee n.º 77/08 que forma parte de la deliberación». La UNILA, universidad pública de la región sur brasileña que coordina y proporciona profesorado de castellano a las escuelas estatales de Río Grande del Sur, Santa Catarina y Paraná, cooficializó el español.

