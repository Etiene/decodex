Para el año 2000, la previsión era que solo en Estados Unidos el número de hispanohablantes alcanzara los 35 000 000. En ese año el español superó al inglés como el idioma más hablado del mundo occidental.308 En 2001, los hispanoparlantes eran aproximadamente 400 millones de personas.309

